			&nbsp&nbsp  巴基斯坦中国针灸中心是中国医生在巴基斯坦卫生管理部门注册的中西医结合治疗和化验诊断中心, 建立于1991年，是一个以中西医结合治疗为主，具有中医特色，设有先进的化验室，以及放射科,心电图和超声波（B超）检查室等。中国医生是来自甘肃省中医院的深资医生，巴方医生均为高资历著名专家，是一个高水平的精英团队，20多年来服务于在巴基斯坦居住的中国人,外国人和本地人，受到了各国患者的欢迎。 2016年12月由甘肃省卫计委和甘肃省中医院挂牌为”巴基斯坦岐黄中医中心”。曾受到中国中央电视台，巴基斯坦ATV，IndusTV电视台，人民日报，光明日报，巴基斯坦民族报等中巴媒体的关注和报道（见网站新闻）。 

